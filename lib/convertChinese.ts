// 轉換簡體為繁體
export const convertToTraditional = (text: string): string => {
  if (!text) return text
  
  let result = text
  
  // 先處理完整的詞彙
  for (const [simplified, traditional] of Object.entries(simplifiedToTraditional)) {
    result = result.replace(new RegExp(simplified, 'g'), traditional)
  }
  
  return result
}

